package Json;

public class Offer {

    private String artist;
    private String dato;
    private int pris;

    public String getArtist() {
        return artist;
    }

    public String getDato() {
        return dato;
    }

    public int getPris() {
        return pris;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public void setDato(String dato) {
        this.dato = dato;
    }

    public void setPris(int pris) {
        this.pris = pris;
    }
}
